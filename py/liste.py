def vide(l):
    """
        renvoie True/False selon que la liste l est vide ou non
    """
    return l==[]


def prem(l):
    """
        renvoie le premier élément de l pour toute liste l non vide
    """
    return l[0]
    
    
def reste(l):
    """
        renvoie le reste de l pour toute liste l non vide
    """
    return l[1:]
    
    
